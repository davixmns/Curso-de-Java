package logica_de_programacao.aula_05;

public class Main {
    public static void main(String[] args) {
        //int -> double
        int i1 = 5;
        double d1 = (double) i1;
        System.out.println(d1);

        //double -> int
        double d2 = 4.3;
        int i2 = (int) d2;
        System.out.println(i2);

        //char -> int
        char c1 = 'd';
        int i3 = (int) c1;
        System.out.println(i3);

        //int -> char
        int i4 = 33;
        char c2 = (char) i4;
        System.out.println(c2);
    }
}
